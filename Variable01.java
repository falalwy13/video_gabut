public class Variable01 {
    public static void main(String[] args) {
        // deklarasi variabel
        String Nama; 
        String NIM;
        
        // mengisi nilai pada variabel
        NIM = "2311016210020";
        Nama = "Muhammad Naufal Alwy"; 
        
        // tulis ke layar
        System.out.print("Nama : ");
        System.out.println(Nama);
        System.out.print("NIM : ");
        System.out.println(NIM);
        
    }
}
